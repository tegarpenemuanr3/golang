package main

import "fmt"

func main() {

	// * + Penjumlahan 
	// * - Pengurangan
	// * * Perkalian
	// * / Pembagian
	// * % Modulus
	// * ++ increment operator
	// * -- decrement operator

	var angka1 int = 10
	var angka2 int = 5

	hasil := angka1 + angka2
	fmt.Println(hasil)

	nomor := 10
	nomor++
	fmt.Println(nomor)
}

